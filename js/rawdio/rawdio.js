var scroll = new PerfectScrollbar('.scroll');

var wavesurfer = WaveSurfer.create({
  container: '#waveform',
  waveColor: '#FFFFFF',
  progressColor: '#974B9A',
  barWidth: 5,
  hideScrollbar: false
}); 

// load default track
wavesurfer.load('https://ia800508.us.archive.org/15/items/LoveThemeFromTheGodfather/02LoveThemeFromTheGodfather.mp3');

wavesurfer.on('play', function () {
    $('#playpause').html('<i class="fa fa-pause"></i>');
})
wavesurfer.on('pause', function () {
  $('#playpause').html('<i class="fa fa-play"></i>');
})

// on waveform ready
wavesurfer.on('waveform-ready', function () {
wavesurfer.play();
});

